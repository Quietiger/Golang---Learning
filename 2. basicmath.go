package main

import "fmt"

func main() {
	fmt.Println("Sum =", 2+2)
	fmt.Println("Difference=", 2-2)
	fmt.Println("Product=", 2*2)
	fmt.Println("Reminder=", 2/2)
	fmt.Println("Modulus=", 2%2)
	fmt.Println(2 ^ 2)
	fmt.Println(2 & 2)
	fmt.Println(2 | 2)
	fmt.Println(2 == 2)
	fmt.Println(2 != 2)
	fmt.Println(2 < 2)
	fmt.Println(2 <= 2)
	fmt.Println(2 > 2)
	fmt.Println(2 >= 2)

}
